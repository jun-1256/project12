
public class Sample2 {

	public static void main(String[] args) {
		// while文の基礎
		
		// 初期値が5で、1ずつ減る場合
		int i = 5;
		while (i > 0) {
			System.out.println(i);
			i--;
		}
		
		System.out.println("\n");
		
		// 初期値が10で、1ずつ減る場合
		int a = 10;
		while (a >= 5) {
			System.out.println(a);
			a--;
		}

	}

}
